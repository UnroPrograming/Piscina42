#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;

	if (nb < 0)
	{
		nb *= -1;
	}
	i = nb - 1;
	if (nb == 1 || nb == 0)
	{
		return (0);
	}
	while (i >= 2)
	{
		if (nb % i == 0)
		{
			return (0);
		}
		i--;
	}
	return (1);
}
